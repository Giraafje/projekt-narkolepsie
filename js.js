// ESS - Designed to measure general level of daytime sleepiness.

// Scoring - Propensity for dozing is rated for each situation on a 4-point scale, from 0, indicating “would never doze,” to 3, indicating a “high chance of dozing.” Adding the scores for each of the 8 questions yields a total score ranging from 0-24.

// Interpretation - An ESS score >10 suggests excessive daytime sleepiness (EDS). An ESS score ≥16 suggests a high level of EDS. Scores within this range are generally associated with significant sleep disorders, including narcolepsy. A high ESS score is suggestive of EDS only and is not diagnostic for a specific sleep disorder. Patients with EDS (ie, ESS >10) may need to be evaluated for a potential sleep disorder, including narcolepsy.


// SNS - is a brief subjective questionnaire that screens for the occurrence of several behavioral symptoms that may be associated with narcolepsy with cataplexy.

// Scoring - Frequency for each behavioral complaint is rated on a 5-point scale, from 1, indicating “never,” to 5, indicating “almost always.” Each question is weighted by a positive or negative factor, with the score calculated using the following equation: (6×Q1 + 9×Q2 – 5×Q3– 11×Q4 – 13×Q5 + 20).

// Interpretation - An SNS score <0 is suggestive of narcolepsy with cataplexy.

// Validation - In 1 study of patients with narcolepsy with cataplexy, an SNS score <0 was shown to have a sensitivity of 96% and specificity of 98%.


